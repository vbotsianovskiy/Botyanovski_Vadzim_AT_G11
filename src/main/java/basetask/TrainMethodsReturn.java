package main.java.basetask;

public class TrainMethodsReturn {

    public int returnNewInt(int i) {
        return (i * 3);
    }

    public long returnNewLong(long i) {
        return (i - 4);
    }

    public char returnNewChar(char i) {
        return i;
    }

    public float returnNewFloat(float i) {
        return (i / 2);
    }

    public double returnNewDouble(double i) {
        return (i + 8);
    }

    public short returnNewShort(short i) {
        return (short) (i - 1);
    }

    public byte returnNewByte(byte i) {
        return (byte) (i * 2);
    }

    public boolean returnNewBoolean(boolean i) {
        return !i;
    }   //вопрос

}
